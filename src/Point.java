/**
 * Point class by coordinates x,y
 */
class Point {
    private Double x;
    private Double y;

    public Point(Double x, Double b) {
        this.x = x;
        this.y = b;
    }

    public Double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }
}
